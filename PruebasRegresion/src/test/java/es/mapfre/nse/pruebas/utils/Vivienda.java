package es.mapfre.nse.pruebas.utils;

public class Vivienda {
	
	private String coPostal;
	private String anoConstruccion;
	private String m2Vivienda;
	
	public String getCoPostal() {
		return coPostal;
	}
	
	public void setCoPostal(String coPostal) {
		this.coPostal = coPostal;
	}
	
	public String getAnoConstruccion() {
		return anoConstruccion;
	}
	
	public void setAnoConstruccion(String anoConstruccion) {
		this.anoConstruccion = anoConstruccion;
	}
	
	public String getM2Vivienda() {
		return m2Vivienda;
	}
	
	public void setM2Vivienda(String m2Vivienda) {
		this.m2Vivienda = m2Vivienda;
	}
	
	
}
